package module.bean;

/**
 * 错误码配置
 * @author 张钰
 *
 */
public class ErrorMap {
	private String sztCode;		//深证通错误码
	private String zlCode;		//直联错误码
	private String sdbCode;		//存管错误码
	private String errExplain;	//错误说明
	
	
	public String getSztCode() {
		return sztCode;
	}
	
	public void setSztCode(String sztCode) {
		this.sztCode = sztCode;
	}
	
	public String getZlCode() {
		return zlCode;
	}
	
	public void setZlCode(String zlCode) {
		this.zlCode = zlCode;
	}
	
	public String getSdbCode() {
		return sdbCode;
	}
	
	public void setSdbCode(String sdbCode) {
		this.sdbCode = sdbCode;
	}
	
	public String getErrExplain() {
		return errExplain;
	}
	
	public void setErrExplain(String errExplain) {
		this.errExplain = errExplain;
	}
		

	
}
