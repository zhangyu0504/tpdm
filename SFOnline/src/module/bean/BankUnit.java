package module.bean;

/**
 * 银行机构信息
 * @author 张钰
 */
public class BankUnit {
	private String depId;	//机构号
	private String branchId;//所属分行行号
	private String depName;	//名称
	private String addr;	//联系地址
	private String zip;		//邮编编码
	private String phone;	//电话
	private String fax;		//传真
	private String conName;	//联系人姓名
	private String conPhone;//联系人电话
	private String quashFlag;//撤消标志 1：运营 2：撤消
	private String comDepId; //并入机构编号
	public String getDepId() {
		return depId;
	}
	public void setDepId(String depId) {
		this.depId = depId;
	}
	public String getBranchId() {
		return branchId;
	}
	public void setBranchId(String branchId) {
		this.branchId = branchId;

	}
	public String getDepName() {
		return depName;
	}
	public void setDepName(String depName) {
		this.depName = depName;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getZip() {
		return zip;
	}
	public void setZip(String zip) {
		this.zip = zip;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getFax() {
		return fax;
	}
	public void setFax(String fax) {
		this.fax = fax;
	}
	public String getConName() {
		return conName;
	}
	public void setConName(String conName) {
		this.conName = conName;
	}
	public String getConPhone() {
		return conPhone;
	}
	public void setConPhone(String conPhone) {
		this.conPhone = conPhone;
	}
	public String getQuashFlag() {
		return quashFlag;
	}
	public void setQuashFlag(String quashFlag) {
		this.quashFlag = quashFlag;
	}
	public String getComDepId() {
		return comDepId;
	}
	public void setComDepId(String comDepId) {
		this.comDepId = comDepId;
	}
	
}
